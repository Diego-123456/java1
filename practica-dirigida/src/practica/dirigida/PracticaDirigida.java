public class PracticaDirigida {

    /**
     * Arreglo
     * @param args
     */
    public static void main(String[] args) {
        //Arreglo unidimensional de 9 elementos 
        float num[] = {12, 45, 13, 67, 1.62f, 7, 14, 9, 56.7f};
        //Listar el arreglo
        int i;
        for (i = 0; i <= num.length - 1; i++) {
            System.out.println(num[i]);
        }
    }
    
}
